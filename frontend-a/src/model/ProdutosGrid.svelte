  import ModalCriarProdutoAdmin from "../components/produtos/ModalCriarProdutoAdmin.svelte";
  import ModalAtualizarProdutoAdmin from "../components/produtos/ModalAtualizarProdutoAdmin.svelte";
  import type { Categoria, Produto } from "../lib/types/produtos";
  import { url } from "../lib/url";

  const getProdutos = () => {
    const res = fetch(url.produtos, {
      method: "GET",
      mode: "cors",
    })
      .then(async (res) => {
        const body = await res.json();
        if (res.status === 200) {
          const produtos = body;
          if (!produtos) {
            toast.error("Erro ao carregar produtos");
          }
          return produtos as Produto[];
        }
      })
      .catch(async (err) => {
        console.log(err);
        return undefined;
      });

    return res;
  };

  const getCategorias = async () => {
    const res = await fetch(url.categorias, {
      method: "GET",
      mode: "cors",
    })
      .then(async (res) => {
        const body = await res.json();
        if (res.status === 200) {
          const categorias = body as Categoria[];
          return categorias;
        }
      })
      .catch(async (err) => {
        console.log(err);
        return undefined;
      });

    return res;
  };

  let criarProdutoModal = false;
  let atualizarProdutoModal = false;

  let IdProduto: string | undefined;
</script>

{#await getCategorias()}
  <div />
{:then categorias}
  <ModalAtualizarProdutoAdmin
    bind:formModal={atualizarProdutoModal}
    {categorias}
    bind:IdProduto
  />
  <ModalCriarProdutoAdmin bind:formModal={criarProdutoModal} {categorias} />
{/await}

<section class="bg-gray-50 dark:bg-gray-900 py-3 sm:py-5">
