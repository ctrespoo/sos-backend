            >
              <path
                clip-rule="evenodd"
                fill-rule="evenodd"
                d="M10 3a1 1 0 011 1v5h5a1 1 0 110 2h-5v5a1 1 0 11-2 0v-5H4a1 1 0 110-2h5V4a1 1 0 011-1z"
              />
            </svg>
            Adicionar produto
          </button>
        </div>
      </div>
      {#await getProdutos()}
        <h1 class="text-white text-center">Carregando...</h1>
      {:then produtos}
        <TabelaProdutos
          {produtos}
          bind:IdProduto
          bind:formModal={atualizarProdutoModal}
        />
